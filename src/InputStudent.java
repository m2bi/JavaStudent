public class InputStudent {
    String name;
    String major;
    String myinfo;

    public void setName(String name) {
        this.name = name;
    }
    public void setMajor(String major) {
        this.major = major;
    }
    public void setMyInfo(String myinfo) {
        this.myinfo = myinfo;
    }

    public String getName() {
        return name;
    }
    public String getMajor() {
        return major;
    }
    public String getMyInfo() {
        return myinfo;
    }
}
